#include <driver/video/vga/gfx/vga_gfx.h>

void vga_gfx_fill(uint8_t color) {

    for(int32_t y = 0; y < VGA_GFX_HEIGHT; ++y) {

        for(int32_t x = 0; x < VGA_GFX_WIDTH; ++x) {

            vga_gfx_put_pixel(x, y, color);
        }
    }
}